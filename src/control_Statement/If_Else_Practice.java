package control_Statement;

public class If_Else_Practice 
{

	public static void main(String []args)
	{
		
		int a; int b;   //Declaration
		a=10; b=20;
		
		if(b>a)  //Condition
		{
			System.out.println("b is greater than a");
		}
		else
		{
			System.out.println("b is smaller than a");
		}
		
		
		
	}
	
}
